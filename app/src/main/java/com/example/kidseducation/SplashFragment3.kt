/**
 * A simple [Fragment] subclass.
 * Use the [SplashFragment3.newInstance] factory method to
 * create an instance of this fragment.
 */
class SplashFragment3 : Fragment() {

    private var _binding: FragmentSplash3Binding? = null
    private val binding get() = _binding!!

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        _binding = FragmentSplash3Binding.inflate(inflater, container, false)
        return binding.root
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)

        binding.buttonMulai.setOnClickListener {
            val intent = Intent(requireContext(), PreLoginActivity::class.java)
            startActivity(intent)
            requireActivity().finish()
        }
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}